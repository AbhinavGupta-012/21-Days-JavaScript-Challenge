// loops are used to repeat certain blocks of code

// for loop is used to repeat a block of code for a finite time

console.log("Using For Loop");
for(let i = 1; i <= 10; i++){
    console.log(i);
}

// while loop is used to repeat a block of code until the condition specified becomes false
let j = 1;
console.log("Using While Loop");
while (j <= 10){
    console.log(j);
    j++;
}