// Day 2
// use let keyword to declare variables

// use single or double quotation marks to establish strings
let string = "JavaScript";

// write digits to establish number
let number = 21;

// write true or false in lowercase to establish boolean
let bool = true;

// to check if the assignment is correct, use typeof operator
console.log(typeof string, typeof number, typeof bool);

// to concatenate two strings use the + operator
let text = "Challenge";
console.log("Concatenation :", string + text);

// there are many arithmetic operations which include the following
// Addition
let day = 2;
console.log("Addition :", number + day);

// Subtraction
console.log("Subtraction :", number - day);

// Mutiplication
console.log("Multiplication :", number * day);

// Division
console.log("Division :", number / day);

// Exponential
console.log("Exponential :", number ** day);

// Modulus (returns the remainder of the division)
console.log("Modulus :", number % day);

// Logic Operations can be used to check whether a statement is true or false
let toCheck = number > day;
console.log(toCheck)